#  进度条
class Progress (object):
    def __init__(self, window):  # path：实验规划表所在路径
        self.window =window
    def progress(self, name, all_num, num):
        self.window['进度名称'].update(value=name + '：')
        self.window['进度'].update(value="▋" * int(num / all_num * 100 / 1.3))
        self.window['百分比'].update(value="{}%".format(int(num / all_num * 100)))